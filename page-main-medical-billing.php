<script>
document.addEventListener("DOMContentLoaded", function() {
  const tocLinks = document.querySelectorAll('.toc-link');
  const allAnchors = Array.from(document.querySelectorAll('section[id], h3[id], li[id], div[id]'));

  // Smooth scrolling for both section and subheadings
  tocLinks.forEach(link => {
    link.addEventListener('click', function(e) {
      e.preventDefault();
      const target = document.querySelector(this.getAttribute('href'));
      if (target) {
        const targetPosition = target.getBoundingClientRect().top + window.pageYOffset;
        const scrollY = targetPosition - (window.innerHeight / 2) + (target.offsetHeight / 2);
        window.scrollTo({
          top: scrollY,
          behavior: 'smooth'
        });
      }
    });
  });

  // Highlight active section or subheading on scroll
  window.addEventListener('scroll', () => {
    let current = '';
    allAnchors.forEach(anchor => {
      const top = anchor.offsetTop - 150; // adjust offset for sticky header or spacing
      if (window.scrollY >= top) {
        current = anchor.getAttribute('id');
      }
    });

    tocLinks.forEach(link => {
      link.classList.remove('text-[#2a54a0]', 'font-semibold');
      if (link.getAttribute('href') === `#${current}`) {
        link.classList.add('text-[#2a54a0]', 'font-semibold');
      }
    });
  });
});
</script>

