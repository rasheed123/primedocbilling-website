<!-- Key Terms List -->
<ul class="list-none space-y-8">

  <li id="claim">
    <h3 class="text-2xl font-bold text-[#3873CC] mb-2">Claim</h3>
    <p>
      A claim is a formal request submitted by healthcare providers to insurance companies 
      for payment of services rendered to patients. It contains all the details regarding 
      treatment and charges. The insurance company reviews the claim and reimburses according 
      to the patient’s coverage.
    </p>
  </li>

  <li id="deductible">
    <h3 class="text-2xl font-bold text-[#3873CC] mb-2">Deductible</h3>
    <p>
      A deductible is the amount of money that a patient must pay annually before their insurance 
      coverage begins. Once this amount is paid, the insurance company starts covering the 
      remaining eligible medical services.
    </p>
  </li>

  <li id="edi">
    <h3 class="text-2xl font-bold text-[#3873CC] mb-2">Electronic Data Interchange (EDI)</h3>
    <p>
      EDI is the electronic transmission of healthcare data between providers and insurers. 
      It is a secure digital network that speeds up the process and reduces paperwork, 
      replacing traditional manual systems.
    </p>
  </li>

  <li id="eft">
    <h3 class="text-2xl font-bold text-[#3873CC] mb-2">Electronic Funds Transfer (EFT)</h3>
    <p>
      EFT is a secure digital method of transferring funds directly from the insurance company 
      to the healthcare provider. This ensures faster reimbursements, greater accuracy, 
      and replaces paper-based check payments.
    </p>
  </li>

  <li id="era">
    <h3 class="text-2xl font-bold text-[#3873CC] mb-2">Electronic Remittance Advice (ERA)</h3>
    <p>
      ERA is a digital document sent by insurance companies that outlines the details of claim 
      processing, including:
    </p>
    <ul class="list-disc pl-6 mt-2 space-y-1">
      <li>Billed amount</li>
      <li>Allowed amount</li>
      <li>Deductibles</li>
      <li>Payments made</li>
      <li>Patient responsibility</li>
      <li>Reason codes</li>
    </ul>
    <p class="mt-2">
      ERA helps healthcare providers and medical billers understand post-payment details 
      and reconcile accounts more accurately.
    </p>
  </li>

  <li id="copay">
    <h3 class="text-2xl font-bold text-[#3873CC] mb-2">Co-pay</h3>
    <p>
      A co-pay is a fixed amount a patient must pay at the time of service. After that, 
      the insurance company covers the remaining cost. Co-pays vary depending on the 
      service type and insurance plan.
    </p>
  </li>

  <li id="coinsurance">
    <h3 class="text-2xl font-bold text-[#3873CC] mb-2">Coinsurance</h3>
    <p>
      Coinsurance is a cost-sharing arrangement where patients and insurers split the cost 
      of medical expenses after the deductible has been met — typically expressed as a percentage.
    </p>
  </li>

  <li id="adjudication">
    <h3 class="text-2xl font-bold text-[#3873CC] mb-2">Adjudication</h3>
    <p>
      Adjudication is the process by which an insurance company reviews, approves, or denies 
      a medical claim based on the policy terms, coverage limits, and medical necessity of the treatment.
    </p>
  </li>

  <li id="global-period">
    <h3 class="text-2xl font-bold text-[#3873CC] mb-2">Global Period</h3>
    <p>
      The global period is a timeframe set by insurance companies during which all routine care 
      related to a procedure is covered under a single payment. It simplifies billing and 
      prevents duplicate charges for the same treatment.
    </p>
  </li>

  <li id="mac">
    <h3 class="text-2xl font-bold text-[#3873CC] mb-2">Medicare Administrative Contractor (MAC)</h3>
    <p>
      MACs are private organizations hired by Medicare to process claims and handle appeals. 
      Each MAC operates within a designated geographic region to ensure compliance and efficiency.
    </p>
  </li>

  <li id="mbi">
    <h3 class="text-2xl font-bold text-[#3873CC] mb-2">Medicare Beneficiary Identifier (MBI)</h3>
    <p>
      MBI is a unique, randomly generated number assigned to Medicare beneficiaries. 
      Its primary purpose is to protect patient identities and ensure secure claim processing.
    </p>
  </li>

  <li id="clearinghouse">
    <h3 class="text-2xl font-bold text-[#3873CC] mb-2">Clearinghouse</h3>
    <p>
      Clearinghouses act as intermediaries between healthcare providers and insurance companies. 
      They review claims for errors, ensure compliance with payer-specific requirements, 
      and streamline submissions to enable faster claim approvals.
    </p>
  </li>

</ul>

