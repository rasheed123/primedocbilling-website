  <section id="components" class="py-8 bg-[#f8fbff] w-full">
    <div class="max-w-5xl mx-auto px-6">
      <h2 class="text-3xl font-extrabold text-[#0a1a3b] mb-6">What are the Components of a Superbill?</h2>

      <p>
        A superbill acts as the foundation for generating accurate insurance claims. The following are the key components of a superbill, along with their functions:
      </p>

      <h3 id="provider-information" class="text-2xl font-semibold text-[#3873CC] mt-8 mb-3">Provider Information</h3>
      <p>
        This section identifies the healthcare facility or professional that delivered the services to the patient. The purpose of the provider’s information is to ensure that insurance companies can verify and confirm the provider’s identity before authorizing billing for services. It includes details like:
      </p>
      <ul class="list-disc pl-6 mt-2 space-y-1">
        <li>Healthcare provider’s complete name and credentials</li>
        <li>Healthcare practice’s name</li>
        <li>National Provider Identifier (NPI) and Tax ID number</li>
        <li>Healthcare facility’s address and contact details</li>
        <li>Healthcare provider’s signature</li>
      </ul>

      <h3 id="patient-information" class="text-2xl font-semibold text-[#3873CC] mt-8 mb-3">Patient Information</h3>
      <p>
        This section includes all the necessary details about the patient receiving care. It links the superbill and insurance claim to the correct insurance plan for reimbursement. It includes the following details:
      </p>
      <ul class="list-disc pl-6 mt-2 space-y-1">
        <li>Patient’s complete name and address</li>
        <li>Insurance provider and policy number</li>
        <li>Patient’s relationship with the insured family member</li>
      </ul>

      <h3 id="date-of-service" class="text-2xl font-semibold text-[#3873CC] mt-8 mb-3">Date of Service</h3>
      <p>
        DOS (Date of Service) specifies when the healthcare services were provided to the patient. It helps insurance companies determine whether the services were rendered during the patient’s active coverage period. It also ensures that the insurance claims are processed under the correct billing date.
      </p>
      <p>
        The purpose of this section is to ensure the alignment of insurance eligibility with the policy dates. It also supports claim accuracy and audit compliance.
      </p>

      <h3 id="procedure-codes" class="text-2xl font-semibold text-[#3873CC] mt-8 mb-3">Procedure Codes (CPT/HCPCS)</h3>
      <p>
        The standardized Current Procedural Terminology (CPT) or Healthcare Common Procedure Coding System (HCPCS) codes describe the medical services performed by the provider. These codes allow the insurance companies to identify the exact services for billing and reimbursement purposes.
      </p>

      <h3 id="diagnosis-codes" class="text-2xl font-semibold text-[#3873CC] mt-8 mb-3">Diagnosis Codes (ICD-10)</h3>
      <p>
        International Classification of Diseases codes identify the patient’s diagnosis. This explains why the service was performed on the patient. Identifying the medical necessity of a procedure is essential for accurate reimbursement.
      </p>

      <h3 id="description-of-services" class="text-2xl font-semibold text-[#3873CC] mt-8 mb-3">Description of Services</h3>
      <p>
        This part provides a brief explanation of the services rendered by the patient. It helps offer a context for the coded information, supporting clarity and transparency in the billing process.
      </p>

      <h3 id="total-charges" class="text-2xl font-semibold text-[#3873CC] mt-8 mb-3">Total Charges</h3>
      <p>
        This section lists the total billed amount for the services rendered. It represents the healthcare provider’s actual charges before any insurance adjustments, patient payments, or discounts.
      </p>

      <h3 id="payment-information" class="text-2xl font-semibold text-[#3873CC] mt-8 mb-3">Payment Information</h3>
      <p>
        This section records the payments made by the patient at the time of service, like copayments or deductibles. This helps in recording and identifying the due payments from the insurance company’s side.
      </p>

      <h3 id="provider-signature" class="text-2xl font-semibold text-[#3873CC] mt-8 mb-3">Provider Signature</h3>
      <p>
        A superbill needs to be signed by the healthcare provider to validate its accuracy and authenticity. The provider’s signature confirms that the services were actually performed with accurate details.
      </p>
    </div>
  </section>

