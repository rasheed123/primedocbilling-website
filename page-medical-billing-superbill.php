      <h2 class="text-3xl font-extrabold text-[#0a1a3b] mb-6">What are the submission methods of a superbill?</h2>

      <p>A superbill can be submitted in several ways to the insurance companies. It depends on the provider’s workflow and the insurance plan of the patients. The following are the submission methods of a superbill:</p>

      <ul class="list-disc pl-6 mt-3 space-y-2">
        <li>The Patient-Submitted method — patient submits a printed or digital superbill directly to the insurance company.</li>
        <li>The Provider-Submitted method — the provider’s billing team electronically submits the superbill through a clearinghouse. It ensures faster processing and fewer errors.</li>
        <li>Patient Portal Submission — some EHRs allow patients to upload superbills directly via online portals.</li>
        <li>Fax or Mail via the Provider Office — for older insurance systems, the provider prints the superbill and faxes it to the insurance company.</li>
      </ul>
